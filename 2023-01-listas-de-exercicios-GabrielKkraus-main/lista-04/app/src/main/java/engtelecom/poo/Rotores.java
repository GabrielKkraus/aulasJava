package engtelecom.poo;

public class Rotores {

    private int rotorUm;
    private int rotorDois;
    private int rotorTres;
    private int rotorQuatro;

    public Rotores(int rotorUm, int rotorDois, int rotorTres, int rotorQuatro) {
        this.rotorUm = rotorUm;
        this.rotorDois = rotorDois;
        this.rotorTres = rotorTres;
        this.rotorQuatro = rotorQuatro;
    }

    public void setRotorUm(int rotorUm) {
        this.rotorUm = rotorUm;
    }

    public void setRotorDois(int rotorDois) {
        this.rotorDois = rotorDois;
    }

    public void setRotorTres(int rotorTres) {
        this.rotorTres = rotorTres;
    }

    public void setRotorQuatro(int rotorQuatro) {
        this.rotorQuatro = rotorQuatro;
    }

}
